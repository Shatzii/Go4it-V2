import { NextResponse } from 'next/server';
import { db } from '@/lib/db';
import { colleges, sportsPrograms, coachingStaff } from '@/shared/schema';

// API to populate ALL NCAA, NAIA, and Junior College institutions
export async function POST() {
  try {
    // Comprehensive list of all college athletic programs
    const comprehensiveCollegeData = await getAllCollegeInstitutions();

    let inserted = 0;
    let errors = 0;

    for (const college of comprehensiveCollegeData) {
      try {
        // Insert college
        const [insertedCollege] = await db.insert(colleges).values(college).returning();

        // Insert sports programs for this college
        if (college.sportsOffered && insertedCollege?.id) {
          const sportsData = college.sportsOffered.map((sport: any) => ({
            collegeId: insertedCollege.id,
            sport: sport.name,
            gender: sport.gender,
            scholarshipsAvailable: sport.scholarships || 0,
            scholarshipsOffered: sport.scholarships || 0,
            season: sport.season,
          }));

          if (sportsData.length > 0) {
            await db.insert(sportsPrograms).values(sportsData);
          }
        }

        // Insert coaching staff for this college
        if (college.coaches && insertedCollege?.id) {
          const coachingData = college.coaches.map((coach: any) => ({
            collegeId: insertedCollege.id,
            sport: coach.sport,
            gender: coach.gender || 'men',
            firstName: coach.firstName,
            lastName: coach.lastName,
            title: coach.title,
            email: coach.email,
            phone: coach.phone,
            recruitingEmail: coach.recruitingEmail,
            dataSource: 'NCAA_Directory',
          }));

          if (coachingData.length > 0) {
            await db.insert(coachingStaff).values(coachingData);
          }
        }

        inserted++;
      } catch (error) {
        console.error(`Error inserting ${college.name}:`, error);
        errors++;
      }
    }

    return NextResponse.json({
      success: true,
      message: `Successfully populated comprehensive college database`,
      statistics: {
        totalProcessed: comprehensiveCollegeData.length,
        successfullyInserted: inserted,
        errors: errors,
        divisions: {
          D1: comprehensiveCollegeData.filter((c) => c.division === 'D1').length,
          D2: comprehensiveCollegeData.filter((c) => c.division === 'D2').length,
          D3: comprehensiveCollegeData.filter((c) => c.division === 'D3').length,
          NAIA: comprehensiveCollegeData.filter((c) => c.division === 'NAIA').length,
          NJCAA: comprehensiveCollegeData.filter((c) => c.division === 'NJCAA').length,
        },
      },
    });
  } catch (error) {
    console.error('Error populating comprehensive database:', error);
    return NextResponse.json(
      {
        success: false,
        error: 'Failed to populate comprehensive database',
        details: error instanceof Error ? error.message : 'Unknown error',
      },
      { status: 500 },
    );
  }
}

